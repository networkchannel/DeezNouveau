i18n
  .use(LanguageDetector)
  .use(initReactI18next)
  .init({
    resources,
    fallbackLng: "en",
    supportedLngs: ["en", "fr", "es", "pt", "de", "tr", "nl", "ar"],
    detection: {
      order: ["localStorage", "navigator", "htmlTag"],
      caches: ["localStorage"],
      lookupLocalStorage: "deezlink_lang",
    },
    interpolation: { escapeValue: false },
  });

/* Auto-detect from IP on first visit (no saved pref) */
const savedLang = localStorage.getItem("deezlink_lang");
if (!savedLang) {
  detectLanguageFromIP().then((lang) => {
    if (lang && resources[lang]) {
      i18n.changeLanguage(lang);
      localStorage.setItem("deezlink_lang", lang);
    }
  }).catch(() => {});
}

