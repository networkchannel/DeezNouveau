/* ════════════════════════════════════════════════════════
   IP-BASED LANGUAGE DETECTION
   ════════════════════════════════════════════════════════ */
async function detectLanguageFromIP() {
  const apis = [
    { url: "https://api.country.is/", parse: (d) => d.country },
    { url: "https://ipwho.is/", parse: (d) => d.country_code },
    { url: "https://ipinfo.io/json", parse: (d) => d.country },
  ];

  for (const api of apis) {
    try {
      const controller = new AbortController();
      const timeout = setTimeout(() => controller.abort(), 3000);
      const res = await fetch(api.url, { signal: controller.signal });
      clearTimeout(timeout);
      if (!res.ok) continue;
      const data = await res.json();
      const country = api.parse(data);
      if (country && COUNTRY_TO_LANG[country]) {
        return COUNTRY_TO_LANG[country];
      }
      // Country found but no mapping → default english
      if (country) return "en";
    } catch {
      continue;
    }
  }
  return null;
}

